class if_else_eg
{
    public static void main(String[] args)
    {
        int num1 = 40;
        int num2 = 50;
        if (num1 > num2)
        {
            System.out.println(num1+" is greater");
        }
        else
        {
            System.out.println(num2+ " is greater");
        }
    }
}